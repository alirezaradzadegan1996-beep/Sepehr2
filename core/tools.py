# core/tools.py

from core.code_engine import run_code_engine


def run_tool(intent, text):
    """
    مرکز اجرای ابزارهای سپهر
    """

    if intent == "ads":
        return ad_tool(text)

    if intent == "design":
        return design_tool(text)

    if intent == "code":
        return run_code_engine(text)

    return "❌ ابزار مناسب پیدا نشد"


# ------------------------
# ابزار تبلیغات
# ------------------------
def ad_tool(text):
    return f"""
📢 پلن تبلیغاتی حرفه‌ای

🎯 محصول/موضوع: {text}

1. هدف کمپین:
- افزایش فروش
- جذب مشتری جدید

2. پیام اصلی:
- تمرکز روی مشکل مشتری
- ارائه راه‌حل سریع

3. کانال‌های تبلیغ:
- اینستاگرام
- واتساپ
- تبلیغات محلی

4. ایده محتوا:
- ویدیو معرفی محصول
- رضایت مشتری
- مقایسه قبل و بعد

🔥 نتیجه: افزایش فروش + اعتماد بیشتر
"""


# ------------------------
# ابزار طراحی
